package com.apibo2doc.main.models;

public class AddressesModel {

    public String name;
    public String pointerAddress;
    public String bufferAddress;
    public String size;

    public AddressesModel(){
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setPointerAddress(String pointerAddress) {
        this.pointerAddress = pointerAddress;
    }

    public void setBufferAddress(String bufferAddress) {
        this.bufferAddress = bufferAddress;
    }

    public void setSize(String size) {
        this.size = size;
    }

}

